<h2><span>Quanto dura un annuncio su Presto.it?</span></h2>
<p>Ogni annuncio rimane online per un anno, alla scadenza del tuo annuncio riceverai una mail contenente un link per rinnovare il tuo annuncio gratuitamente.</p>
<p>E' importante che tu faccia questa operazione entro e non oltre 10 giorni dal momento del ricevimento della mail, in caso contrario&nbsp;non avrai la possibilità di rinnovare il tuo annuncio.</p>
